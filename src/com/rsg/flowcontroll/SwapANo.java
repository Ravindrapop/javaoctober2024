package com.rsg.flowcontroll;

public class SwapANo {

	public static void main(String[] args) {
		
		int a = 5;
		
		int b = 6;
		
		int c = 0;
		
		System.out.println("The value of a is : "+a +" The value of b is : "+b );
		
		
		c=a;
		a=b;
		b=c;
		
		System.out.println("The value of a is : "+a +" The value of b is : "+b );

	}

}
